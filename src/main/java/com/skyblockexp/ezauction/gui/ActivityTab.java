package com.skyblockexp.ezauction.gui;

/**
 * Represents the different tabs available in the My Activity menu.
 */
public enum ActivityTab {
    /**
     * Shows the player's active auction listings.
     */
    MY_LISTINGS,
    
    /**
     * Shows the player's active buy orders.
     */
    MY_ORDERS,
    
    /**
     * Shows items waiting to be claimed by the player.
     */
    PENDING_RETURNS,
    
    /**
     * Shows the player's recent transaction history.
     */
    RECENT_HISTORY
}
